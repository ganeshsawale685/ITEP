# 5. If it costs 1600 rs. to fence a rectangular park of length 20 m at the rate of 25 rs. per m², find the breadth of the park and its perimeter. Also, find the area of the field.
total_cost = 1600
rate_per_m = 25
length = 20

perimeter = total_cost / rate_per_m
breadth = (perimeter / 2) - length
area_park = length * breadth

print(f"Breadth of the park: {breadth} m")
print(f"Perimeter of the park: {perimeter} m")
print(f"Area of the park: {area_park} m_square")

